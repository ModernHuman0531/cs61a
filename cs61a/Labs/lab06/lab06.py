def make_fib():
    """Returns a function that returns the next Fibonacci number
    every time it is called.

    >>> fib = make_fib()
    >>> fib()
    0
    >>> fib()
    1
    >>> fib()
    1
    >>> fib()
    2
    >>> fib()
    3
    >>> fib2 = make_fib()
    >>> fib() + sum([fib2() for _ in range(5)])
    12
    >>> from construct_check import check
    >>> # Do not use lists in your implementation
    >>> check(this_file, 'make_fib', ['List'])
    True
    """
    "*** YOUR CODE HERE ***"
    n_0, n_1, times = 0, 1, 0
    def fib():
        nonlocal n_0, n_1, times
        if times == 0:
            times += 1
            return n_0
        elif times == 1:
            times += 1
            return n_1
        else:
            n_0, n_1 = n_1, n_0 + n_1
            return n_1
    return fib


def insert_items(lst, entry, elem):
    """
    >>> test_lst = [1, 5, 8, 5, 2, 3]
    >>> new_lst = insert_items(test_lst, 5, 7)
    >>> new_lst
    [1, 5, 7, 8, 5, 7, 2, 3]
    >>> large_lst = [1, 4, 8]
    >>> large_lst2 = insert_items(large_lst, 4, 4)
    >>> large_lst2
    [1, 4, 4, 8]
    >>> large_lst3 = insert_items(large_lst2, 4, 6)
    >>> large_lst3
    [1, 4, 6, 4, 6, 8]
    >>> large_lst3 is large_lst
    True
    """
    index = 0
    while index < len(lst):#this will check multiple time
        if lst[index] == entry:
            lst.insert(index + 1, elem)
            if entry == elem:#for special case, if don't take this case, it will stuck in infinite loop
                index += 1
        index += 1
    return lst
            


